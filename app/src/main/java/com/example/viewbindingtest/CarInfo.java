public class CarInfo {
    public String Name;
    public String Price;
    public String Miles;
    public String Description;
    public int Image;

    public CarInfo(String name, String price, String miles, String description, int image) {
        Name = name;
        Price = price;
        Miles = miles;
        Description = description;
        Image = image;
    }
}
